const statusByDate = (endDate: string): ContractStatus => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);

  const end = new Date(endDate);
  end.setHours(0, 0, 0, 0);

  if (end < today) return 'vencido';
  if (end > today) return 'agendado';
  return 'ativo';
};

